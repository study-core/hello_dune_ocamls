    由于对象类型匹配，它们 【可以】被放在同一个列表中
    val obj2 : unrelated_class = <obj>
*)
let obj2 = new unrelated_class;;


(* 
      - : unrelated_class list = [<obj>; <obj>]

      ********************************************************************
      直接定义的 obj  和 使用 class 定义的 obj2 由于类型一致:
      ********************************************************************

      ********************************************************************
      都具备 方法:  set : int -> int -> unit end   
      ********************************************************************

      故被认为是相同的对象类型
      可以放到同一个 list 中
