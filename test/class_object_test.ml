(* 
    
    直接定义 object， 语法等同 class 的 = 号后面的定义

    val obj : < set : int -> int -> unit > = <obj>    


    该类型仅由其公共方法定义。值不可见，私有方法也不可见（未显示）。与记录不同，这种类型不需要  显式预定义。  【查看  object  和 record 区别】


    ********************************************************************
    注意: 具有满足这个定义的方法 的 class 被认为是 相同的 对象类型
    ********************************************************************
