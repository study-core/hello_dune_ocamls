let obj = 
  object (self)
    val mutable x = 0
    val mutable y = 0
    method set new_x new_y = (x <- new_x; y <- new_y)
  end;;


(* - : unit = () *)
obj#set 1 2;;


(* 
********************************************************************  
对比下这句话: 

具有满足这个定义的方法 的 class 被认为是 相同的 对象类型

class 类型不是 【数据类型】，称为【类型】。 object 类型是一种【数据类型】，就像记录类型或元组一样。


【定义 class 时，同时定义了同名的 class 类型和 object 类型】


首先，我们定义上面与obj无关的类 
********************************************************************
*)
