------------------------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------
  (即，可以在  mli 文件中 的定义: class point : int -> int -> object .. end, 在  ml 文件中 的定义: class point ini_x ini_y = object .. end )

  class point :
    int ->
    int ->
